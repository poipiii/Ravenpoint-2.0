function CreateColumns(columns:string[]) {
  if (columns == undefined) {
    return []
  }
  else{
     return columns.map((col_name, index) => {
        return {
             Header: col_name,
             accessorKey: col_name,
             cell: ({ row }:any) => <div className="lowercase">{row.getValue(col_name)}</div>,


        }

    })

  }
  
}



